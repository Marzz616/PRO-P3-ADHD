            <!--Vraag 1-->
            <div class="faq-question">Wat is het verschil tussen ADHD en ADD?</div>
            <!--Als je op de blok klikt, dan komt de vraag open-->
            <div class="faq-answer">ADHD staat voor Attention Deficit Hyperactivity Disorder, terwijl ADD staat voor Attention Deficit Disorder. 
                                    Het belangrijkste verschil is dat mensen met ADHD ook hyperactief zijn, terwijl mensen met ADD dat niet per se zijn.</div>
            <!--Vraag 2-->
            <div class="faq-question">Wat zijn de belangrijkste symptomen van ADHD en ADD?</div>
            <div class="faq-answer">ADHD-symptomen omvatten hyperactiviteit, impulsiviteit en moeite met aandacht vasthouden. 
                                    ADD-symptomen zijn gericht op moeite met aandacht en concentratie zonder hyperactiviteit.</div>
            <!--Vraag 3-->
            <div class="faq-question">Wanneer wordt ADHD/ADD meestal gediagnosticeerd?</div>
            <div class="faq-answer">Het wordt vaak gediagnosticeerd in de kindertijd, maar het kan ook volwassenen treffen. 
                                    Diagnose vindt meestal plaats na uitgebreid onderzoek door gezondheidsprofessionals.</div>
            <!--Vraag 4-->
            <div class="faq-question">Hoe kan ik omgaan met ADHD/ADD op het werk?</div>
            <div class="faq-answer">Om goed om te gaan met ADHD/ADD op het werk, is het belangrijk om een gestructureerde omgeving te creëren, taken in kleinere stukjes te verdelen, 
                                    en te communiceren met collega's en leidinggevenden over eventuele ondersteuningsbehoeften.</div>
            <!--Vraag 5-->    
            <div class="faq-question">Zijn er specifieke sporten die kunnen helpen bij het beheersen van ADHD/ADD symptomen?</div>
            <div class="faq-answer">Ja, sporten zoals yoga, zwemmen, en martial arts kunnen helpen bij het beheersen van ADHD/ADD symptomen door het bevorderen van concentratie, 
                                    rust en lichaamsbewustzijn.</div>
            <!--Vraag 6-->        
            <div class="faq-question">Wat zijn enkele tips voor ouders van kinderen met ADHD/ADD?</div>
            <div class="faq-answer">Enkele tips voor ouders van kinderen met ADHD/ADD zijn: het handhaven van een consistente routine, het bieden van duidelijke instructies, 
                                    het prijzen van positief gedrag, en het zoeken naar ondersteuning van gezondheidsprofessionals en andere ouders.</div>
            <!--Vraag 7-->
            <div class="faq-question">Hoe wordt ADHD/ADD behandeld?</div>
            <div class="faq-answer">Behandeling omvat vaak een combinatie van gedragstherapie, medicatie en ondersteunende maatregelen. 
                                    Het is belangrijk om samen met een gezondheidsprofessional een behandelingsplan op te stellen.</div>
            <!--Vraag 8-->
            <div class="faq-question">Welke medicijnen worden vaak voorgeschreven voor ADHD/ADD?</div>
            <div class="faq-answer">Stimulerende medicijnen zoals methylfenidaat (Ritalin) en amfetamines worden vaak voorgeschreven. 
                                    Niet-stimulerende medicijnen zoals atomoxetine kunnen ook worden gebruikt.</div>
            <!--Vraag 9-->
            <div class="faq-question">Hoe beïnvloedt ADHD/ADD het dagelijks leven?</div>
            <div class="faq-answer">ADHD/ADD kan invloed hebben op schoolprestaties, werk, relaties en zelfs het dagelijkse functioneren. 
                                    Het is belangrijk om strategieën te ontwikkelen om met deze uitdagingen om te gaan.</div>
            <!--Vraag 10-->
            <div class="faq-question">Zijn er specifieke dieetmaatregelen voor mensen met ADHD/ADD?</div>
            <div class="faq-answer">Hoewel er geen specifiek dieet is voor ADHD/ADD, sommige mensen melden verbeteringen met een uitgebalanceerd dieet, 
                                    rijk aan omega-3 vetzuren en laag in toegevoegde suikers.</div>
            <!--Vraag 11-->
            <div class="faq-question">Is ADHD/ADD erfelijk?</div>
            <div class="faq-answer">Er is een genetische component; het komt vaker voor bij mensen wiens familieleden ook ADHD/ADD hebben.</div>
            <!--Vraag 12-->
            <div class="faq-question">Kunnen mensen met ADHD/ADD succesvol zijn in hun carrière?</div>
            <div class="faq-answer">Absoluut! Met de juiste ondersteuning en strategieën kunnen mensen met ADHD/ADD succesvol zijn in verschillende beroepen.</div>
            <!--Vraag 13-->
            <div class="faq-question">Welke ondersteuning is beschikbaar voor mensen met ADHD/ADD?</div>
            <div class="faq-answer">Ondersteuning omvat educatieve aanpassingen, coaching, therapie en gemeenschapsbronnen. 
                                    Het is belangrijk om een sterk ondersteunend netwerk te hebben.</div>
            <!--Vraag 14-->
            <div class="faq-question">Hoe kan ik mijn concentratie verbeteren?</div>
            <div class="faq-answer">Concentratie kan verbeterd worden door het verminderen van afleiding, het creëren van een georganiseerde werkruimte, 
                                    en het gebruik van technieken zoals time management en mindfulness.</div>
            <!--Vraag 15-->
            <div class="faq-question">Zijn er alternatieve behandelingen voor ADHD/ADD?</div>
            <div class="faq-answer">Sommige mensen proberen alternatieve behandelingen zoals acupunctuur, meditatie, en voedingssupplementen, 
                                    maar het is belangrijk om deze met een gezondheidsprofessional te bespreken.</div>
            <!--Vraag 16-->
            <div class="faq-question">Hoe kan ik mijn kinderen ondersteunen als ze ADHD/ADD hebben?</div>
            <div class="faq-answer">Het is belangrijk om open communicatie te behouden, onderwijs aan te passen aan individuele behoeften, 
                                    en ondersteuning te bieden bij het ontwikkelen van copingvaardigheden.</div>
            <!--Vraag 17-->
            <div class="faq-question">Wat zijn enkele mogelijke complicaties van ADHD/ADD?</div>
            <div class="faq-answer">Mogelijke complicaties zijn onder andere problemen op school of werk, moeilijkheden in sociale relaties, 
                                    en comorbiditeiten zoals angststoornissen of depressie.</div>
            <!--Vraag 18-->
            <div class="faq-question">Hoe kan ik het beste omgaan met de emotionele uitdagingen van ADHD/ADD?</div>
            <div class="faq-answer">Het kan nuttig zijn om emotionele ondersteuning te zoeken, stressmanagementtechnieken te leren, 
                                    en te werken aan zelfacceptatie en zelfcompassie.</div>
            <!--Vraag 19-->
            <div class="faq-question">Zijn er specifieke slaapproblemen geassocieerd met ADHD/ADD?</div>
            <div class="faq-answer">Slaapproblemen zoals slapeloosheid, rusteloze slaap, en slaapapneu kunnen voorkomen bij mensen met ADHD/ADD.</div>
            <!--Vraag 20-->            
            <div class="faq-question">Hoe kan ik als partner van iemand met ADHD/ADD het beste ondersteunen?</div>
            <div class="faq-answer">Het is belangrijk om geduldig te zijn, open communicatie te behouden, 
                                    en samen te werken aan strategieën voor het omgaan met uitdagingen.</div>
            <!--Vraag 21-->
            <div class="faq-question">Wat zijn enkele strategieën voor het omgaan met impulsiviteit?</div>
            <div class="faq-answer">Enkele strategieën om impulsiviteit te beheersen zijn het nemen van pauzes voor het nemen van beslissingen, 
                                    het visualiseren van de mogelijke gevolgen van acties, en het aanleren van ontspanningstechnieken zoals diepe ademhaling.</div>
            <!--Vraag 22-->
            <div class="faq-question">Zijn er verschillende vormen van ADHD/ADD?</div>
            <div class="faq-answer">Ja, er zijn verschillende subtypes van ADHD, waaronder het overwegend onoplettende type, 
                                    het overwegend hyperactieve-impulsieve type, en het gecombineerde type.</div>
            <!--Vraag 23-->
            <div class="faq-question">Kan ADHD/ADD worden veroorzaakt door voeding?</div>
            <div class="faq-answer">Er is geen directe oorzaak-en-gevolgrelatie tussen voeding en ADHD/ADD, 
                                    maar sommige mensen melden verergering van symptomen na consumptie van bepaalde voedingsmiddelen of toevoegingen.</div>
            <!--Vraag 24-->
            <div class="faq-question">Welke rol spelen omgevingsfactoren bij ADHD/ADD?</div>
            <div class="faq-answer">Omgevingsfactoren zoals blootstelling aan gifstoffen tijdens de zwangerschap, vroeggeboorte, roken tijdens de zwangerschap, 
                                    en blootstelling aan lood kunnen het risico op het ontwikkelen van ADHD/ADD verhogen.</div>
            <!--Vraag 25-->
            <div class="faq-question">Hoe kan ik een gezonde levensstijl bevorderen voor iemand met ADHD/ADD?</div>
            <div class="faq-answer">Een gezonde levensstijl omvat regelmatige lichaamsbeweging, voldoende slaap, een uitgebalanceerd dieet, en stressmanagementtechnieken.</div>
            <!--Vraag 26-->
            <div class="faq-question">Wat zijn enkele veelvoorkomende misvattingen over ADHD/ADD?</div>
            <div class="faq-answer">Enkele veelvoorkomende misvattingen zijn dat ADHD/ADD een teken van luiheid is, dat het alleen bij kinderen voorkomt, 
                                    en dat het kan worden 'genezen' door simpelweg meer discipline.</div>
            <!--Vraag 27-->
            <div class="faq-question">Hoe kan ik mijn kinderen helpen omgaan met de uitdagingen van ADHD/ADD op school?</div>
            <div class="faq-answer">Ouders kunnen samenwerken met leraren en schooldeskundigen om individuele ondersteuningsplannen te ontwikkelen, 
                                    educatieve aanpassingen aan te vragen en te zorgen voor een ondersteunende leeromgeving thuis.</div>
            <!--Vraag 28-->
            <div class="faq-question">Is er een verband tussen ADHD/ADD en slaapstoornissen?</div>
            <div class="faq-answer">Ja, slaapstoornissen komen vaak voor bij mensen met ADHD/ADD en kunnen de symptomen verergeren. 
                                    Het handhaven van een consistente slaaproutine kan helpen.</div>
            <!--Vraag 29-->
            <div class="faq-question">Welke rol spelen neurotransmitters bij ADHD/ADD?</div>
            <div class="faq-answer">Neurotransmitters zoals dopamine en norepinefrine spelen een belangrijke rol bij het reguleren van aandacht, 
                                    stemming en motivatie, en verstoringen in hun functie worden geassocieerd met ADHD/ADD.</div>
            <!--Vraag 30-->
            <div class="faq-question">Kan het gebruik van beeldschermen de symptomen van ADHD/ADD beïnvloeden?</div>
            <div class="faq-answer">Er is enig bewijs dat overmatig gebruik van beeldschermen, zoals tv, computers en smartphones, de symptomen van ADHD/ADD kan verergeren. 
                                    Het is belangrijk om schermtijd te beperken en regelmatige pauzes in te lassen.</div>
